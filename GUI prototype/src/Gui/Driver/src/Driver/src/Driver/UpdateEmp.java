           int ID=Integer.parseInt(id.getText());
          String name= ename.getText();
         String number=num.getText();
         String Email=email.getText();
         String pass=pwd.getText();
         boolean flag= false;
        String str="";
       flag= e.setName(name);
       if(flag==false)
       {
           str=str+ " Name \n";
           
       }
        flag=e.setContact(number);
        if(flag==false)
        {
            str= str + " Contact \n";
        }
        flag=e.setEmail(Email);
        if(flag==false)
        {
            str= str + " Email \n";
        }
        flag=e.setEpass(pass);
        if(flag==false)
        {
            str= str + " Password \n";
        }
      if(str.equals(""))
      {
          Driver.getInstance().getEmp().set(ID,e);
          JOptionPane.showMessageDialog(null, "Data is successfully updated");
           AdminMenu m =new AdminMenu();
           m.setVisible(true);
           this.setVisible(false);
        }
       else
        {
            JOptionPane.showMessageDialog(null, "Following credentials are invalid:"+ str+ "\nInput these again");
            
            
        }
      
       
          
        
        

         

        
        
