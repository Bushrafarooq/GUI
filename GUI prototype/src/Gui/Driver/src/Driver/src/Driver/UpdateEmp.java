    private void OKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OKActionPerformed
        // TODO add your handling code here:
        int ID=Integer.parseInt(id.getText());
        ID=ID-1;
        if(ID>=0&&ID<Driver.getInstance().getEmp().size())
        {
            this.ename.setText(Driver.getInstance().getEmp().get(ID).getName());
            this.email.setText(Driver.getInstance().getEmp().get(ID).getEmail());
            this.num.setText(Driver.getInstance().getEmp().get(ID).getContact());
           this.pwd.setText(Driver.getInstance().getEmp().get(ID).getEpass());
            
        }
        else
        {
            JOptionPane.showMessageDialog(null,"ID not found");
        }
        
    }//GEN-LAST:event_OKActionPerformed

